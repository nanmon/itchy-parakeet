    private void jbCerrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCerrarActionPerformed
        try {
            for(int i=0; i<alumnos.size(); ++i){
                escritor.guardarDatos(alumnos.get(i).toString());
            }
        } catch (Exception ex) {
            Logger.getLogger(AltaDeAlumno.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
    }//GEN-LAST:event_jbCerrarActionPerformed

    private void jbBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBorrarActionPerformed
        jtfNombre.setText("");
        jtfExpediente.setText("");
        jcbCiclo.setSelectedIndex(0);
        jtfCurp.setText("");
        jtfCorreo.setText("");
    }//GEN-LAST:event_jbBorrarActionPerformed

    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed
        alumnos.add(new Alumno(
                jtfNombre.getText(),
                jtfExpediente.getText(),
                jcbCiclo.getSelectedIndex(),
                jtfCurp.getText(),
                jtfCorreo.getText()
        ));
        jbBorrarActionPerformed(evt);
    }//GEN-LAST:event_jbAgregarActionPerformed

